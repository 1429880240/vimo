<script type="text/javascript">
  import { clamp } from '../../../src/util/util'

  export default {
    name: 'DemoSetBarStyle',
    i18n: {
      messages: {
        'zh-CN': {
          title: '设置Bar的样式',
          notice: '如果在微信/支付宝/钉钉模式下, 可能看不到Header的效果.'
        },
        'en-US': {
          title: 'Set Bar Style',
          notice: 'If you are in WeChat/Alipay/Stick mode, you may not see the effect of Header.'
        }
      }
    },
    computed: {
      headerComponent () {
        return this.$refs.header
      }
    },
    methods: {
      onScrollHandler (ev) {
        const TOP = 200
        const precent = 1 - clamp(0, ev.scrollTop / TOP, 1)
        const style = {
          opacity: precent,
          visibility: precent <= 0 ? 'hidden' : 'visible'
        }
        this.headerComponent.setStyle(style)
      }
    }
  }
</script>
