<script type="text/javascript">
  import { clamp } from '../../../src/util/util'

  export default {
    name: 'setBarStyle',
    computed: {
      headerComponent () {
        return this.$refs.header
      }
    },
    methods: {
      onScrollHandler (ev) {
        const TOP = 200
        const precent = 1 - clamp(0, ev.scrollTop / TOP, 1)
        const style = {
          opacity: precent,
          visibility: precent <= 0 ? 'hidden' : 'visible'
        }
        this.headerComponent.setStyle(style)
      }
    }
  }
</script>
